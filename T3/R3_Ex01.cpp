/*Faça um algoritmo que mostre seu nome na tela 5 vezes utilizando laços de repetição*/

#include <iostream>

int main() {
    for(int x=0;x<5;x++) {  //expressão inicial (x = 0), expressão de condição (x < 5) e expressão de loop (x++)
        std::cout<<"Felipe \n"; //A repetição de "Felipe" ocorre 5 vezes porque o laço é válido do 0 ao 4.
    }
    return 0;
}
