/*Faça um algoritmo que mostre seu nome na tela 500 vezes utilizando laços de repetição*/

#include <iostream>

int main() {
    for(int x=0;x<500;x++) {  //expressão inicial (x = 0), expressão de condição (x < 500) e expressão de loop (x++)
        std::cout<<"Felipe\n"; //A repetição de "Felipe" ocorre 500 vezes porque o laço é válido do 0 ao 499.
    }
    return 0;
}
