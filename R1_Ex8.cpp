/*Faça um algoritmo que pegue a quantidade de cartinhas que uma pessoa tem e mostre na tela*/

#include <cstdio>

int main () {
    //Declaração de variável
    int cartinhas;

    //Entrada de dados
    printf("Insira a quantidade de cartinhas: ");
    scanf("%d", &cartinhas);

    //Saída de dados
    printf("Você tem %d cartinhas", cartinhas);

    return 0;
}
