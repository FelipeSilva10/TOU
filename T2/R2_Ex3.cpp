/*
Faça um algoritmo que diga se uma pessoa tem poucas ou muitas tarefas.
SE A PESSOA TIVER 5 OU MAIS TAREFAS, ELA TEM MUITAS TAREFAS. SENÃO ELA TEM POUCAS TAREFAS
 */

#include <iostream>

int main() {
 //Declaração de variáveis
 int T;
 //Entrada de dados
 std::cout<<"Digite a quantidade de tarefas: ";
 std::cin>>T;
 //Lógica e Saída de dados
 if(T>=5) {
  std::cout<<"Você tem muitas tarefas!";
 }
 else {
  std::cout<<"Você tem muitas tarefas!";
 }

 return 0;
}
